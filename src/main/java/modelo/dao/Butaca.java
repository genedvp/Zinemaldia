package modelo.dao;

public class Butaca {

	private int idButaca;
	private int precioButaca;
	private int columna;
	private int fila;

	public int getIdButaca() {
		return idButaca;
	}

	public void setIdButaca(int idButaca) {
		this.idButaca = idButaca;
	}

	public int getPrecioButaca() {
		return precioButaca;
	}

	public void setPrecioButaca(int precioButaca) {
		this.precioButaca = precioButaca;
	}

	public int getColumna() {
		return columna;
	}

	public void setColumna(int columna) {
		this.columna = columna;
	}

	public int getFila() {
		return fila;
	}

	public void setFila(int fila) {
		this.fila = fila;
	}

}
